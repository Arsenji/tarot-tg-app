export const allCards: TarotCard[] = [...majorArcana, ...minorArcana];

export const getRandomCard = (): TarotCard => {
  const randomIndex = Math.floor(Math.random() * allCards.length);
  const selectedCard = allCards[randomIndex];
  console.log(`getRandomCard - Index: ${randomIndex}, Card: ${selectedCard.name}, Total cards: ${allCards.length}`);
  return selectedCard;
};

export const getCardsByCategory = (category: 'love' | 'career' | 'personal'): TarotCard[] => {
  switch (category) {
    case 'love':
      return allCards.filter(card => card.suit === "Кубки" || card.isMajorArcana);
    case 'career':
      return allCards.filter(card => card.suit === "Пентакли" || card.isMajorArcana);
    case 'personal':
      return majorArcana;
    default:
      return allCards;
  }
};
